void main() {
  // Integer data type
  int age = 30;

  // Double data type
  double height = 5.9;

  // String data type
  String name = 'John Doe';

  // List data type (list of integers)
  List<int> numbers = [1, 2, 3, 4, 5];

  // Map data type (mapping names to ages)
  Map<String, int> ages = {
    'John': 30,
    'Jane': 25,
    'Bob': 35,
  };

  // Print statements to demonstrate each data type
  print('Age: $age');
  print('Height: $height');
  print('Name: $name');
  print('Numbers: $numbers');
  print('Ages: $ages');
}
